static std::vector<double> eval_field(
    const std::function<double(double,double,double)>& fn,
    double min_x, double min_y, double min_z,
    double dx, double dy, double dz, int nx, int ny, int nz) {
    int nxg = nx + 1, nyg = ny + 1, nzg = nz + 1;
    std::vector<double> field(nxg * nyg * nzg);
    for (int i = 0; i <= nx; i++)
        for (int j = 0; j <= ny; j++)
            for (int k = 0; k <= nz; k++)
                field[i * nyg * nzg + j * nzg + k] =
                    fn(min_x + i * dx, min_y + j * dy, min_z + k * dz);
    return field;
}

static Mesh marching_cubes_from_field(const std::vector<double>& field,
                                       const BoundingBox& box, int nx, int ny, int nz,
                                       double isovalue) {
    Point mn = box.min_point();
    Point mx = box.max_point();
    double min_x = mn[0], min_y = mn[1], min_z = mn[2];
    double dx = (mx[0] - min_x) / nx;
    double dy = (mx[1] - min_y) / ny;
    double dz = (mx[2] - min_z) / nz;

    int nxg = nx + 1, nyg = ny + 1, nzg = nz + 1;
    std::vector<int> edge_x(nx * nyg * nzg, -1);
    std::vector<int> edge_y(nxg * ny * nzg, -1);
    std::vector<int> edge_z(nxg * nyg * nz, -1);

    auto ex = [&](int a, int b, int c) -> int& { return edge_x[a * nyg * nzg + b * nzg + c]; };
    auto ey = [&](int a, int b, int c) -> int& { return edge_y[a * ny * nzg + b * nzg + c]; };
    auto ez = [&](int a, int b, int c) -> int& { return edge_z[a * nyg * nz + b * nz + c]; };

    Mesh mesh;
    std::vector<double> vpos;
    std::vector<double> vacc;

    for (int i = 0; i < nx; i++) {
        for (int j = 0; j < ny; j++) {
            for (int k = 0; k < nz; k++) {
                double pt[8][3] = {
                    {min_x + i * dx,       min_y + j * dy,       min_z + k * dz},
                    {min_x + (i+1) * dx,   min_y + j * dy,       min_z + k * dz},
                    {min_x + (i+1) * dx,   min_y + (j+1) * dy,   min_z + k * dz},
                    {min_x + i * dx,       min_y + (j+1) * dy,   min_z + k * dz},
                    {min_x + i * dx,       min_y + j * dy,       min_z + (k+1) * dz},
                    {min_x + (i+1) * dx,   min_y + j * dy,       min_z + (k+1) * dz},
                    {min_x + (i+1) * dx,   min_y + (j+1) * dy,   min_z + (k+1) * dz},
                    {min_x + i * dx,       min_y + (j+1) * dy,   min_z + (k+1) * dz},
                };
                double val[8] = {
                    field[i * nyg * nzg + j * nzg + k],
                    field[(i+1) * nyg * nzg + j * nzg + k],
                    field[(i+1) * nyg * nzg + (j+1) * nzg + k],
                    field[i * nyg * nzg + (j+1) * nzg + k],
                    field[i * nyg * nzg + j * nzg + (k+1)],
                    field[(i+1) * nyg * nzg + j * nzg + (k+1)],
                    field[(i+1) * nyg * nzg + (j+1) * nzg + (k+1)],
                    field[i * nyg * nzg + (j+1) * nzg + (k+1)],
                };

                int cube_idx = 0;
                for (int v = 0; v < 8; v++)
                    if (val[v] < isovalue) cube_idx |= (1 << v);
                if (cube_idx == 0 || cube_idx == 255) continue;

                auto make_vertex = [&](int e) -> int {
                    int* sp = nullptr;
                    switch (e) {
                        case 0:  sp = &ex(i,   j,   k  ); break;
                        case 1:  sp = &ey(i+1, j,   k  ); break;
                        case 2:  sp = &ex(i,   j+1, k  ); break;
                        case 3:  sp = &ey(i,   j,   k  ); break;
                        case 4:  sp = &ex(i,   j,   k+1); break;
                        case 5:  sp = &ey(i+1, j,   k+1); break;
                        case 6:  sp = &ex(i,   j+1, k+1); break;
                        case 7:  sp = &ey(i,   j,   k+1); break;
                        case 8:  sp = &ez(i,   j,   k  ); break;
                        case 9:  sp = &ez(i+1, j,   k  ); break;
                        case 10: sp = &ez(i+1, j+1, k  ); break;
                        case 11: sp = &ez(i,   j+1, k  ); break;
                        default: return -1;
                    }
                    if (*sp >= 0) return *sp;
                    int ca = EDGE_VERTICES[e][0];
                    int cb = EDGE_VERTICES[e][1];
                    double va = val[ca], vb = val[cb];
                    double t = (std::abs(vb - va) < 1e-12) ? 0.5 : (isovalue - va) / (vb - va);
                    double px = pt[ca][0] + t * (pt[cb][0] - pt[ca][0]);
                    double py = pt[ca][1] + t * (pt[cb][1] - pt[ca][1]);
                    double pz = pt[ca][2] + t * (pt[cb][2] - pt[ca][2]);
                    size_t vk = mesh.add_vertex(Point(px, py, pz));
                    vpos.push_back(px); vpos.push_back(py); vpos.push_back(pz);
                    vacc.push_back(0.0); vacc.push_back(0.0); vacc.push_back(0.0);
                    *sp = (int)vk;
                    return *sp;
                };

                const int* tris = TRI_TABLE[cube_idx];
                for (int t = 0; t < 15; t += 3) {
                    if (tris[t] == -1) break;
                    int v0 = make_vertex(tris[t]);
                    int v1 = make_vertex(tris[t + 1]);
                    int v2 = make_vertex(tris[t + 2]);
                    if (v0 >= 0 && v1 >= 0 && v2 >= 0) {
                        mesh.add_face({(size_t)v0, (size_t)v2, (size_t)v1});
                        double ax=vpos[v0*3],ay=vpos[v0*3+1],az=vpos[v0*3+2];
                        double bx=vpos[v1*3],by=vpos[v1*3+1],bz=vpos[v1*3+2];
                        double cx=vpos[v2*3],cy=vpos[v2*3+1],cz=vpos[v2*3+2];
                        double ue=bx-ax,uf=by-ay,ug=bz-az;
                        double ve=cx-ax,vf=cy-ay,vg=cz-az;
                        double fnx=uf*vg-ug*vf,fny=ug*ve-ue*vg,fnz=ue*vf-uf*ve;
                        vacc[v0*3]-=fnx; vacc[v0*3+1]-=fny; vacc[v0*3+2]-=fnz;
                        vacc[v1*3]-=fnx; vacc[v1*3+1]-=fny; vacc[v1*3+2]-=fnz;
                        vacc[v2*3]-=fnx; vacc[v2*3+1]-=fny; vacc[v2*3+2]-=fnz;
                    }
                }
            }
        }
    }
    size_t nv = vacc.size() / 3;
    for (size_t vi = 0; vi < nv; ++vi) {
        double vnx=vacc[vi*3], vny=vacc[vi*3+1], vnz=vacc[vi*3+2];
        double len = std::sqrt(vnx*vnx + vny*vny + vnz*vnz);
        if (len > 1e-12)
            mesh.vertex[vi].set_normal(vnx/len, vny/len, vnz/len);
    }
    mesh.halfedge.clear();
    mesh.clear_pointcolors();
    mesh.clear_facecolors();
    mesh.clear_linecolors();
    return mesh;
}

static Mesh marching_cubes(const std::function<double(double,double,double)>& fn,
                            const BoundingBox& box, int nx, int ny, int nz,
                            double isovalue) {
    Point mn = box.min_point();
    Point mx = box.max_point();
    double min_x = mn[0], min_y = mn[1], min_z = mn[2];
    double dx = (mx[0] - min_x) / nx;
    double dy = (mx[1] - min_y) / ny;
    double dz = (mx[2] - min_z) / nz;
    auto field = eval_field(fn, min_x, min_y, min_z, dx, dy, dz, nx, ny, nz);
    return marching_cubes_from_field(field, box, nx, ny, nz, isovalue);
}

static Mesh merge_meshes(const Mesh& m1, const Mesh& m2) {
    auto [v1, f1] = m1.to_vertices_and_faces();
    auto [v2, f2] = m2.to_vertices_and_faces();
    size_t offset = v1.size();
    std::vector<Point> verts;
    verts.insert(verts.end(), v1.begin(), v1.end());
    verts.insert(verts.end(), v2.begin(), v2.end());
    std::vector<std::vector<size_t>> faces;
    for (auto& f : f1) faces.push_back(f);
    for (auto& f : f2) {
        std::vector<size_t> nf;
        for (size_t idx : f) nf.push_back(idx + offset);
        faces.push_back(nf);
    }
    Mesh result = Mesh::from_vertices_and_faces(verts, faces);
    result.halfedge.clear();
    result.clear_pointcolors();
    result.clear_facecolors();
    result.clear_linecolors();
    return result;
}

double MeshIso::eval(TpmsType type, double x, double y, double z, double period) {
    double X = x / period;
    double Y = y / period;
    double Z = z / period;
    switch (type) {
        case TpmsType::GYROID:
            return std::sin(X)*std::cos(Y) + std::sin(Y)*std::cos(Z) + std::sin(Z)*std::cos(X);
        case TpmsType::SCHWARZ_P:
            return std::cos(X) + std::cos(Y) + std::cos(Z);
        case TpmsType::DIAMOND:
            return std::sin(X)*std::sin(Y)*std::sin(Z)
                 + std::sin(X)*std::cos(Y)*std::cos(Z)
                 + std::cos(X)*std::sin(Y)*std::cos(Z)
                 + std::cos(X)*std::cos(Y)*std::sin(Z);
        case TpmsType::NEOVIUS:
            return 3.0*(std::cos(X)+std::cos(Y)+std::cos(Z))
                 + 4.0*std::cos(X)*std::cos(Y)*std::cos(Z);
        case TpmsType::IWP:
            return 2.0*(std::cos(X)*std::cos(Y)+std::cos(Y)*std::cos(Z)+std::cos(Z)*std::cos(X))
                 - (std::cos(2.0*X)+std::cos(2.0*Y)+std::cos(2.0*Z));
        case TpmsType::LIDINOID:
            return std::sin(2.0*X)*std::cos(Y)*std::sin(Z)
                 + std::sin(2.0*Y)*std::cos(Z)*std::sin(X)
                 + std::sin(2.0*Z)*std::cos(X)*std::sin(Y)
                 + std::cos(2.0*X)*std::cos(2.0*Y)
                 + std::cos(2.0*Y)*std::cos(2.0*Z)
                 + std::cos(2.0*Z)*std::cos(2.0*X);
        case TpmsType::FISCHER_KOCH_S:
            return std::cos(2.0*X)*std::sin(Y)*std::cos(Z)
                 + std::cos(2.0*Y)*std::sin(Z)*std::cos(X)
                 + std::cos(2.0*Z)*std::sin(X)*std::cos(Y);
        case TpmsType::FRD:
            return 4.0*std::cos(X)*std::cos(Y)*std::cos(Z)
                 - (std::cos(2.0*X)*std::cos(2.0*Y)
                  + std::cos(2.0*Y)*std::cos(2.0*Z)
                  + std::cos(2.0*Z)*std::cos(2.0*X));
        case TpmsType::PMY:
            return 2.0*std::cos(X)*std::cos(Y)*std::cos(Z)
                 + std::sin(2.0*X)*std::sin(Y)
                 + std::sin(X)*std::sin(2.0*Z)
                 + std::sin(2.0*Y)*std::sin(Z);
        default:
            return 0.0;
    }
}

Mesh MeshIso::from_tpms(TpmsType type, const BoundingBox& box,
                         int nx, int ny, int nz,
                         double isovalue, double period,
                         TpmsMode mode, double thickness) {
    auto fn = [type, period](double x, double y, double z) {
        return eval(type, x, y, z, period / (2.0 * Tolerance::PI));
    };
    if (mode == TpmsMode::SOLID) {
        return marching_cubes(fn, box, nx, ny, nz, isovalue);
    }
    Point mn = box.min_point();
    Point mx = box.max_point();
    double min_x = mn[0], min_y = mn[1], min_z = mn[2];
    double dx = (mx[0] - min_x) / nx;
    double dy = (mx[1] - min_y) / ny;
    double dz = (mx[2] - min_z) / nz;
    auto field = eval_field(fn, min_x, min_y, min_z, dx, dy, dz, nx, ny, nz);
    Mesh m1 = marching_cubes_from_field(field, box, nx, ny, nz, isovalue - thickness);
    Mesh m2 = marching_cubes_from_field(field, box, nx, ny, nz, isovalue + thickness);
    return merge_meshes(m1, m2);
}

Mesh MeshIso::from_function(std::function<double(double,double,double)> fn,
                             const BoundingBox& box, int nx, int ny, int nz,
                             double isovalue) {
    return marching_cubes(fn, box, nx, ny, nz, isovalue);
}

double MeshIso::sdf_sphere(double cx, double cy, double cz, double r,
                            double x, double y, double z) {
    double dx = x - cx, dy = y - cy, dz = z - cz;
    return std::sqrt(dx*dx + dy*dy + dz*dz) - r;
}

double MeshIso::sdf_box(double cx, double cy, double cz,
                         double hx, double hy, double hz,
                         double x, double y, double z) {
    double qx = std::abs(x - cx) - hx;
    double qy = std::abs(y - cy) - hy;
    double qz = std::abs(z - cz) - hz;
    double mx = std::max(qx, 0.0);
    double my = std::max(qy, 0.0);
    double mz = std::max(qz, 0.0);
    return std::sqrt(mx*mx + my*my + mz*mz) + std::min(std::max({qx, qy, qz}), 0.0);
}

double MeshIso::sdf_capsule(const Point& p0, const Point& p1, double r,
                             double x, double y, double z) {
    double pax = x - p0[0], pay = y - p0[1], paz = z - p0[2];
    double bax = p1[0] - p0[0], bay = p1[1] - p0[1], baz = p1[2] - p0[2];
    double h = std::max(0.0, std::min(1.0, (pax*bax + pay*bay + paz*baz) / (bax*bax + bay*bay + baz*baz)));
    double dx = pax - h * bax, dy = pay - h * bay, dz = paz - h * baz;
    return std::sqrt(dx*dx + dy*dy + dz*dz) - r;
}

double MeshIso::sdf_torus(double cx, double cy, double cz,
                           double major_r, double minor_r,
                           double x, double y, double z) {
    double lx = x - cx, ly = y - cy, lz = z - cz;
    double q = std::sqrt(lx*lx + ly*ly) - major_r;
    return std::sqrt(q*q + lz*lz) - minor_r;
}

double MeshIso::sdf_plane(double nx, double ny, double nz, double d,
                           double x, double y, double z) {
    return nx*x + ny*y + nz*z - d;
}

double MeshIso::smooth_union(double a, double b, double k) {
    return -std::log(std::exp(-k * a) + std::exp(-k * b)) / k;
}

double MeshIso::smooth_subtract(double a, double b, double k) {
    return -smooth_union(-a, b, k);
}

double MeshIso::smooth_intersect(double a, double b, double k) {
    return -smooth_union(-a, -b, k);
}

